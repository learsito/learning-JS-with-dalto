//Operadores de comparación: Comparan dos expresiones y devuelven un valor Boolean que representa la relación de sus valores. Si la comparación es correcta devolverá un true, y si no lo es un false.
a == b //Igualdad O igual que
a != b //Desigualdad O diferente de
a === b // Identidad O idéntico a || Compara que sean del mismo tipo de dato a diferencia del ==
a !== b //No identidad O lo contrario a idéntico
a > b // Mayor que
a >= b //Mayor o igual a
a < b //Menor que
a <= b //Menor o igual a
//Operadores lógicos: Nos devuelven un resultado a partir de que se cumpla (o no) una condición, su resultado es booleano, y sus operandos son valores lógicos o asimilables a ellos.
 a && b // AND lógico (conjunción) | Regresa true si ambos operandos son verdaderos, de lo contrario regresa false.
 a || b // OR lógico (disyunción) | Regresa true si al menos uno de los dos operandos son verdaderos. Si ninguno lo es, regresa falso.
 !a    // NOT lógico (negación) | Niega a su operando por lo que adquiere el valor contrario al que tenía: si es true pasa a ser false y viceversa.
